"""Data landscape endpoint — data quality, metadata, and source overview.

Reads the pre-computed analysis from _analysis_results.json (generated by the
analysis script) and enriches with live DB row counts.  Cached with 24h TTL.
"""
import json
import logging
from pathlib import Path

from fastapi import APIRouter, Depends
from sqlalchemy import text
from sqlalchemy.ext.asyncio import AsyncSession

from src.dependencies import get_db, get_cache
from src.middleware.auth import get_current_user
from src.services.cache import CacheService

logger = logging.getLogger(__name__)
router = APIRouter(prefix="/api/data-landscape", tags=["data-landscape"])

# Pre-computed analysis results (generated by the analysis script)
_APP_DIR = Path(__file__).resolve().parents[3]
ANALYSIS_PATH = _APP_DIR / "_analysis_results.json"
if not ANALYSIS_PATH.exists():
    ANALYSIS_PATH = Path("/app/_analysis_results.json")

CACHE_TTL = 86400  # 24 hours


def _load_analysis() -> dict | None:
    """Load the pre-computed analysis JSON.  Returns None if missing."""
    if not ANALYSIS_PATH.exists():
        return None
    try:
        with open(ANALYSIS_PATH, "r", encoding="utf-8") as f:
            return json.load(f)
    except Exception as exc:
        logger.warning("data_landscape: failed to load analysis JSON: %s", exc)
        return None


async def _get_db_table_counts(db: AsyncSession) -> list[dict]:
    """Query live row counts for key tables."""
    tables = [
        ("dim_occupation", "ESCO occupations"),
        ("dim_skill", "ESCO skills"),
        ("dim_region", "UAE regions/emirates"),
        ("dim_sector", "ISIC sectors"),
        ("dim_institution", "Higher education institutions"),
        ("fact_demand_vacancies_agg", "LinkedIn job postings"),
        ("fact_supply_talent_agg", "FCSC + Bayanat workforce"),
        ("fact_ai_exposure_occupation", "AI exposure scores"),
        ("fact_occupation_skills", "Occupation-skill mappings"),
        ("crosswalk_soc_isco", "SOC-ISCO crosswalk"),
    ]
    results = []
    for table, description in tables:
        try:
            count = (await db.execute(text(f"SELECT count(*) FROM {table}"))).scalar() or 0
            results.append({"table": table, "rows": count, "description": description})
        except Exception:
            results.append({"table": table, "rows": 0, "description": description})
    return results


async def _get_view_counts(db: AsyncSession) -> list[dict]:
    """Query live row counts for materialized views."""
    views = [
        ("vw_supply_talent", "Supply: talent by time/region/occupation"),
        ("vw_demand_jobs", "Demand: jobs by time/region/occupation"),
        ("vw_ai_impact", "AI exposure scores by occupation"),
        ("vw_gap_cube", "Supply-demand gap analysis"),
        ("vw_forecast_demand", "Forecast predictions"),
    ]
    results = []
    for view, description in views:
        try:
            count = (await db.execute(text(f"SELECT count(*) FROM {view}"))).scalar() or 0
            results.append({"view": view, "rows": count, "description": description})
        except Exception:
            results.append({"view": view, "rows": 0, "description": description})
    return results


