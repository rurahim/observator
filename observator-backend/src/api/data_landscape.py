def _build_emiratisation(analysis: dict) -> dict:
    """Extract Emiratisation data from the MOHRE KPIs."""
    mohre = analysis.get("supply", {}).get("mohre_kpis", {})

    emirati_data = mohre.get("emiratis_in_private_sector", {})
    emiratis = []
    for year, val in zip(emirati_data.get("years", []), emirati_data.get("values", [])):
        emiratis.append({"year": year, "value": int(val)})

    nafis_data = mohre.get("nafis_establishments_hiring_emiratis", {})
    nafis = []
    for year, val in zip(nafis_data.get("years", []), nafis_data.get("values", [])):
        nafis.append({"year": year, "value": int(val)})

    return {
        "emiratis_in_private_sector": emiratis,
        "nafis_establishments": nafis,
        "growth_pct": emirati_data.get("growth", 0),
    }


